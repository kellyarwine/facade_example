class Shower
	def shampoo_hair
		puts "Adding shampoo."
	end

	def condition_hair
		puts "Adding conditioner."
	end

	def rinse(body_part)
		puts "Rinsing #{body_part}."
	end

	def turn_shower_on
		puts "The shower is on."
	end

	def turn_shower_off
		puts "The shower is off."
	end

	def lather_washcloth
		puts "Lathering washcloth."
	end

	def apply_soap
		puts "Applying soap."
	end

	def shave(body_part)
		puts "Shaving #{body_part}."
	end
end