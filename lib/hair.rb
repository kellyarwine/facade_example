class Hair
	def turn_blow_dryer_on
		puts "Blow dryer on!"
	end

	def turn_blow_dryer_off
		puts "Blow dryer off!"
	end

	def turn_straightener_on
		puts "Straightener on!"
	end

	def turn_straightener_off
		puts "Straightener off!"
	end

	def partially_blow_dry_hair
		puts "Hair is partially drying."
	end

	def section_hair
		puts "Grabbing section of hair."
	end

	def curl_hair(section)
		puts "Curling section of hair."
	end

	def straighten_hair(section)
		puts "Straightening section of hair."
	end

	def brush_hair
		puts "Brushing hair."
	end
end